import 'auth_api.dart';
import 'auth_model.dart';

class AuthRepository {
  final AuthApi api;

  AuthRepository(this.api);

  Future<AuthUser> login(String email, String password, String loai) {
    return api.login(email: email, password: password, loai: loai);
  }

  Future<void> register(String email, String password, String hoTen, String loai) {
    return api.register(email: email, password: password, hoTen: hoTen, loai: loai);
  }

  Future<void> logout() => api.logout();
}
